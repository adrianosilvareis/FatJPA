    private void iniciarComponentes() {
        dao = new Business();
        dao.getEntityManager().getTransaction().begin();
        listarPesquisa();
    }

    private void listarPesquisa() {
        list = dao.findAll(MascaraConvenio.class);
        mostraPesquisa();
    }

    private void mostraPesquisa() {
        removeDesativado();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        if (list.isEmpty()) {
            ErroMensagens.getInstancia().InformacaoMensagem("Nenhum resultado encontrado");
        } else {
            String[] linha = new String[]{null, null};
            int i = 0;
            for (MascaraConvenio sftConv : list) {
                dtm.addRow(linha);
                dtm.setValueAt(sftConv.getId(), i, 0);
                dtm.setValueAt(sftConv.getDescricao(), i, 1);
                i++;
            }
        }
    }

    public void jTTabelaLinhaSelecionada(JTable masterTable) {
        try{
            convIdIdconvField.setText(String.valueOf(list.get(masterTable.getSelectedRow()).getId()));
            convNmNmconvField.setText(list.get(masterTable.getSelectedRow()).getDescricao());
        } catch(ArrayIndexOutOfBoundsException e){
            convIdIdconvField.setText("");
            convNmNmconvField.setText("");
        }
    }
    
    private void removeDesativado() {
        List<MascaraConvenio> toRemove = new ArrayList<>();
        for (MascaraConvenio sftConv : list) {
            try{if (sftConv.getDescricao().contains("ZZ ")) toRemove.add(sftConv);}catch(NullPointerException e){}
        }
        list.removeAll(toRemove);
    }
