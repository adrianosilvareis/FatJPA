    private void iniciarComponentes() {
        dao = new Business();
        dao.getEntityManager().getTransaction().begin();
        listarPesquisa();
    }

    private void listarPesquisa() {
        list = dao.findAll(SftCtrlAcao.class);
        mostraPesquisa();
    }

    private void mostraPesquisa() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        if (list.isEmpty()) {
            ErroMensagens.getInstancia().AtencaoMensagem("Nenhum resultado encontrado");
        } else {
            String[] linha = new String[]{null, null, null, null};
            int i = 0;
            for (SftCtrlAcao sftAcao : list) {
                dtm.addRow(linha);
                dtm.setValueAt(sftAcao.getAcaoIdIdacao(), i, 0);
                dtm.setValueAt(sftAcao.getAcaoNmNome(), i, 1);
                i++;
            }
        }
    }

    public void jTTabelaLinhaSelecionada(JTable masterTable) {
        try{
            acaoIdIdacaoField.setText(list.get(masterTable.getSelectedRow()).getAcaoIdIdacao() + "");
            acaoNmNomeField.setText(list.get(masterTable.getSelectedRow()).getAcaoNmNome());
        } catch(ArrayIndexOutOfBoundsException e){
            acaoIdIdacaoField.setText("");
            acaoNmNomeField.setText("");
        }
    }
